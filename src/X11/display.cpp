#include <yavin/X11/display.h>
//==============================================================================
namespace yavin::x11 {
//==============================================================================
display::display() : m_x_display{XOpenDisplay(nullptr)} {
  if (m_x_display == nullptr) {
    throw std::runtime_error{"[X] cannot connect server"};
  }
}
//------------------------------------------------------------------------------
display::~display() {
  XCloseDisplay(m_x_display);
}
//==============================================================================
}  // namespace yavin::x11
//==============================================================================
